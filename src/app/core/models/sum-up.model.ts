/**
 * Represents a summary of a country's participation in a sporting event.
 * @interface
 *
 * @author Pignon Pierre-Olivier
 *
 * @version 1.0
 */
export interface SumUp {
  /**
   * Number of participations.
   *
   * @type {number}
   */
  joCount:number;
  /**
   * The number of medals won.
   *
   * @type {number}
   */
  medals:number;
  /**
   * The number of athletes.
   *
   * @type {number}
   */
  athletes:number;
}


